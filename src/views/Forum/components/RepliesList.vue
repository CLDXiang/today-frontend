        面试作品，以产品经理实习生为例，可以写写产品分析报告，竞品分析报告，像一些广告学的同学，你可以做一些广告的分析，设计类的同学则是可以做一些设计作品。但是<b>准备这些作品的时候，一定要注意体现你的专业性！！！</b>
      </p>
      <p>以我见过的同学举例：</p>
      <blockquote>
        我之前见过一个同学去面试游戏公司，人家上去就直接列出来几十款游戏，和面试官说让他挑两款，直接和人面试官讲这个游戏哪不好，为什么不好，从自己的角度怎么去改善，改善的方案是怎么样的，具体怎么落地去做。<br>而我另一个想做游戏策划的同学，我问他为什么想做游戏策划？人说：“我挺喜欢玩游戏的”。然后我说你能给分析一款游戏吗？然后他说了一堆我感觉这个游戏有哪些哪些做的不好的地方。我再问他为什么不好啊？你觉得游戏公司为什么不改这个不好的点呐？那应该怎么改进呢？他就一片茫然。。。
      </blockquote>
      <p>
        再强调一遍，你的面试作品，一定是要能够体现你的专业性！！！哪怕你做的不好都不要紧，绝对不要出现那种我感觉、我以为的主观、业余的现象。
      </p>
      <p>像上述例子的第二个同学，完全就只是在<b>“吐槽“</b>。</p>
      <p>
        没有实习过的同学，没事可以经常写写来练练手，这样面试的时候，也不用急急忙忙的来准备，实习经历、项目经历比较丰富的同学，实际上帮助不是很大，自己斟酌即可。
      </p>
      <p class="ztext-empty-paragraph">
        <br>
      </p>
      <p><b>3、校内实践项目</b></p>
      <p>
        通常来说，所谓的校内实践项目主要是导师项目，但实际上一些普通的同学基本少很难得到这个机会；一是你必须和学校里老师的关系比较好（这个是主要），其次就是要求你的学校成绩比较好（可能）。
      </p>
      <p>
        一般来说，答主还是不建议过多的关注校内的实践项目；因为机会有时候并不好拿（很多时候并不是因为你能力不行），其次是这些项目一般都是由研究生带着做，研究生负责的事务较多，本科生来说，一般都负责的事项都不会很多，很难得到独立承担某些重要事务从而拔高自己的能力。
      </p>
      <p>
        但是答主身边也有那种遇到良师的同学，获得很好的机会，能够在项目中负责很大部分事务，实现自己的想法，得到的锻炼质量很高。
      </p>
      <p>这里，各位同学，就根据自己的实际情况，灵活应变吧~</p>
      <p>反正每每我看到那种遇到良师，得到好机会锻炼的同学，我也只能。。。</p>
