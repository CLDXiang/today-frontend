      <p>剖析楼主问题，也应该是大多数同学担忧的点：</p>
      <ul>
        <li>
          <b>我大学基本上都是在学校里面，就是上上课，考考试，从来没有实习过，当然没有工作经验，自己也没关系，这个时候应该怎么办？</b>
        </li>
      </ul>
      <p>
        实际上，任何人不可能一上来就有实习经历的；但是，“<b>经验”这个东西，不一定只有靠“正式的工作”才能够获得。</b>
      </p>
      <p>
        对于一个实习生来说，所谓“经验”，说的具体一些，就是：做这件事情，你知道该怎么做，具备成为一个合格员工的基本素质，这个基本素质，包括但不限于：
      </p>
      <ol>
        <li>逻辑思考能力</li>
        <li>独立解决问题的能力</li>
        <li>学习能力</li>
        <li>一定量的专业知识及基本技能</li>
      </ol>
      <p>等等。</p>
      <p>
        这些东西总结非常简单，但是<b>到了实际面试中，就是你得用自己实践过的事实进行说明自己如何具备这些点。</b>
      </p>
      <p>从我自己的经历来看，对于在校的大学生，主要可以4个方面的实践：</p>
      <ul>
        <li><b>社团实践</b></li>
        <li><b>面试作品</b></li>
        <li><b>校内实践项目</b></li>
        <li><b>校外实践项目</b></li>
      </ul>
      <p>（以上三点，从上往下的含金量逐级升高）</p>
      <p><b>1、社团实践</b></p>
      <p>注意！这里<b>并不是让你说自己当了XXX主席、XXX部长、XXX书记；</b></p>
      <p>
        <b>着重点是：你作为某活动的承办者，设计活动的思路是怎么样的，最后是达到了一个怎么样的成绩。</b>
      </p>
      <p>以答主自己举例（没有装X的意思，和各位知乎大佬相比，自知差距甚远）：</p>
      <blockquote>
        我在大一下的时候，就接手了我们校区的贵州老乡会，在此之前，老乡会的活跃度比较低，一度处于快要办不下去的地步，我参加换届的时候，加上上一届负责人，到场的只有5个人，且每次老乡会举办活动，参加的的人数不过12、13个左右。<br>但是在我接手过后，老乡会的活跃人数增长一倍。<br>之所以能够增长一倍，是因为我在对社群管理、活动的属性是否契合同学的需求、哪些假期同学们是否空闲、增加老乡对于老乡会的亲和度这几个环节做过思考（具体这里就不展开讲，因为内容过多），并实际的进行实施改善，才达到最后活跃人数的增长一倍的效果。
      </blockquote>
      <p>
        <b>这里我想表达的意思是，不要过于在意学校里的这些头衔，而在于是否真正的提升了你的能力。</b>
      </p>
      <p>大一、大二上的同学在社团工作的话，可以按照我所说的这个思考方向去实践一下，</p>
      <p>
        但是对于大二下、大三之后的同学，建议还是不要过多的花精力，这块虽然能体现你是一个爱思考的人，以及具备团队凝聚力等等，但却很少体现你已经具备能够踏入职场的基本素质。
      </p>
      <p class="ztext-empty-paragraph">
        <br>
      </p>
      <p><b>2、面试作品</b></p>
      <p>这个东西对于在校生找实习公司而言，绝对是很加分的环节！</p>
      <p>一来，这样的准备，比起“我什么都不会，但是我愿意学”这句话，更有诚意。</p>
      <p>二来，让你具备和面试官聊天的“谈资”。</p>
      <p>
        如果你只是一味的讲学校的事情，一般来说面试官不会太感兴趣（除非他和你是一个学校），相比之下，面试作品这种能够具备一点点专业性的东西，更能让面试官搭得上话，让面试官看到你做这种专业性东西时候的思路，如果你做的好，面试官透过你的思路自然就能看出你的能力，如果你做的不好，你还可以把自己的问题提出来，然后请教他，让他看到你确实有思考，而且去做了，但毕竟成长都有一个过程，遇到问题是件很正常的现象；从而证明你的积极性，以及你不是一个只会“张嘴等着别人喂食”的人！
      </p>
