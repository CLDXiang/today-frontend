      <p>这种社会上的实践，大多都是对高效学生开放的，这种实践具有以下特点：</p>
      <p><b>1、能力要求、市场认可度非常之高。</b></p>
      <p>
        一是市场上的知识本身就是远远超前于学校的。学校很多知识都不教，而且企业方也不会告诉你应该学什么，学生需要自己独立去开阔自己的视野并学习这些知识；二是竞争范围变大。以前你可能需要竞争的对象只是学校同学，或者周边大学的同学；但现在你需要面临的全国各地的学生以及一些职场精英。
      </p>
      <p><b>2、公平公正。</b></p>
      <p>
        这种实践，一般不会说是，老师指定哪些人参加，也不会是学校综合成绩前5%的人才能参加；因为这是面向社会，用于企业发现人才的比赛，只要你有能力，都可以参加。
      </p>
      <p><b>3、需要学生信息开放。</b></p>
      <p>
        因为这种比赛通常情况是不会发布到每一个学校里的，一般都只是官网或指定的城市/地区/渠道进行推广，加之很多知识学校也不会教，这就要求学生自己的视野宽广，信息不闭塞。
      </p>
