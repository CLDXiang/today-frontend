      <p>我的习惯，我觉得简历的大概架构分为（按重要性往下排）：</p>
      <ul>
        <li><b>教育背景</b>（如果你是跨专业求职，且自己的学校不好，建议把这块放后面）</li>
        <li><b>实习经历</b></li>
        <li><b>项目/实践经历</b></li>
        <li><b>社团活动</b></li>
        <li><b>技能</b></li>
        <li><b>个人爱好</b></li>
      </ul>
      <p>
        至于个人评价这种东西，一般也不会
        有人看；一是不能说明你有什么能力。你的基本素质从你的实习/项目经历中、从你阐述你解决问题的思路中就可以看出来。二是还不如填写个人爱好，假如你的团队和你有同样的爱好，这样更容易接纳你。
      </p>
      <p>照片的话，同学根据的自己的颜值自己斟酌即可；一般还是建议不放~</p>
      <p>
        在写具体某一块内容时，比如实习经历，<b>要写能力、写经历、成果，</b>那种你觉得特别有货的板块，就要用加粗字体或者其他办法引起面试官注意，从引导面试官的思路；<b>在加粗部分，提前想好面试官在这个点可能会问哪些问题，理清楚自己的逻辑。</b>
      </p>
      <p><b>（2）自我介绍</b></p>
      <p>
        明确一点：<b>以经历为主，个人特点为辅，与职位无关的最好不说。</b>面试之前提前整理好介绍的点，一般情况下，你的自我介绍直接就决定了面试官想和你聊的话题顺序。
      </p>
      <p><b>（3）回答问题</b></p>
      <p>
        在回答面试官提问时，不仅仅只是回答问题。<b>要从问题入手，详细的向对方展现自己切入方式、思考过程以及相应的方案和观点，</b>这才是面试官真正想要了解的东西。
      </p>
      <p>
        在遇到不会的答案时，保持镇静。在面试中，被问到一些自己确实不懂，以至于不知道如何回答。这个时候应该：<b>保持真诚，实事求是，可以顺便带点小技巧，但千万不要乱说一通：</b>
      </p>
      <p>
        要么你可以说以前碰到过的相类似的问题，从类似的角度去回答；要么你可以说你确实不懂，但你觉得可以从哪些角度去进行，并表达自己的学习意愿。
      </p>
      <p><b>（4）实习时间</b></p>
      <p>一般来说，最基本就是3个月，但是建议你还是说半年为好（这也是没办法的事儿~）。</p>
      <p><b>（5）问面试官问题</b></p>
      <p>
        这里不要去问一些什么工资、食堂啊什么的，不是说这些不重要，相比于你的职业发展来说，没有那么的重要。
      </p>
      <p>
        这个环节要记住一点：<b>面试是一个双向选择的过程。</b>你应该着重<b>围绕这个部门的氛围、leader的水平以及培养实习生的思路、可以锻炼的能力项</b>等方面去问问题，因为我们实习的目的，就是在短短几个月的时间内，尽可能多的学到东西。
      </p>
      <p class="ztext-empty-paragraph">
        <br>
      </p>
      <p>从简历制作，到问面试官问题，这整个流程，我感觉我讲的还是有些概括，但大体就是那么几点。</p>
      <p>
        光靠我的这些文字，同学面试可能还是达不到完美的效果；建议先按这几点做下来，先找几个非意向公司面面试，根据面试官的反馈，看看自己哪些环节做的不好，再进行调优；<b>但如果你是简历完全没法写，建议你还是好好看看前面的“前期准备”部分吧，不要只是一味的追求面试技巧、简历技巧。</b>
      </p>
      <p class="ztext-empty-paragraph">
        <br>
      </p>
      <h2>3、实习渠道</h2>
      <p>
        坦白来说，相比前期准备之下，实习渠道不是那么的重要，因为现在平台那么多，信息都比较透明，重要的还是自己的实力。
      </p>
      <p>但总体还是遵循</p>
      <p><b>（1）学长推荐——&gt;社招（难度逐渐升高）</b></p>
      <p><b>（2）大城市——&gt;小城市（数量逐渐减少）</b></p>
      <p>这样的思路。</p>
      <p>
        至于一些具体的平台名称，你可以看看这个问题的其他帖子；其实每个平台包含的信息量大体也都差不多。
      </p>
      <p class="ztext-empty-paragraph">
        <br>
      </p>
      <h2>4、如何做好一个实习生</h2>
      <p>
        <b>永远记住，拿到offer，入了职，这并不意味着你可以有好好舒一口气；入职只是开始；</b>以下几点是我在实习期间经常踩的坑，希望对你有帮助~
      </p>
      <p><b>（1）职业化态度</b></p>
      <p>所谓职业化态度，就是你工作这8个小时，你所产出的工作成果，要对得起公司付给你的钱。</p>
      <p>
        哪怕是一个实习生，也应该把自己当成一个正式员工去做事；不要老是“划水”，不要像在学校里写作业、论文一样，以为随便写写就行；你做的东西，产出的成果，都是要给别人看的，拿给别人用的；进了公司，自己就应该对自己制定一个标准。
      </p>
      <p><b>（2）独立思考、独立解决问题</b></p>
      <p>
        <b>进了企业，要记住这里没有你的老师，你不是来这里跟人学习的，你是来这里给人解决问题的。</b>
      </p>
      <p>
        遇到问题，不要上来就去指这上司问着问那，尽可能自己独立思考解决，不要去拖累上司的工作效率；尤其是一些比较低级的问题，像软件怎么操作这种问题，一定要杜绝！像一些业务上的问题，实在你解决不了，你再去问；问的时候注意你的问题是否定位清楚，去之前先对自己说一遍，看看有没有察觉到什么不对的地方。
      </p>
      <p><b>（3）尽可能快的熟悉团队，熟悉业务</b></p>
      <p>
        通常来说，当你刚进去的时候，上司会给你走过场的带你去认识一下团队，但是那种基本上也就是走个过场；需要你自己平时去了解每个人具体所负责的业务，这样才方面你以后工作上的对接，并且在平时生活你还可以和同事、前辈多沟通，了解不同工种间的情况，以及后面方便你快速搞清楚，你所打的“杂”具体是从哪个环节出现的。
      </p>
      <p><b>（4）沟通准确</b></p>
      <p>
        在和上司或者其他同事沟通之前，先把自己的想法/事情，在脑子里面捋一捋逻辑；先说什，后说什么，然后在自己心里过一遍，看看有什么问题；在面对面沟通时，注意确认对方所理解的事物，是否就等于你想表达的东西，如果你感觉到不对的时候，问对方一句：你理解的xxx是不是XXX？或者是：”我把我的想法讲清楚了吗？“
      </p>
      <p><b>（5）交代事情要记清，没有事情不要开工，出现疑惑，实时同步</b></p>
      <p>
        <b>工作中，可以出现你不知道怎么做，但切记一定要避免“返工”。</b>一是极大的影响你的工作情绪，从而打击你的工作积极性。一两次还好，多了你就会厌恶工作。二是返工不仅仅是耽误你的时间，而且还可能出现耽误协作同事的工作进度。
      </p>
      <p><b>（6）设置一个便签</b></p>
      <p>
        不要用那种纸质的便签，用电子便签；平时上司交代的事情、自己工作中的错误，方便及时记录下来，不会遗忘。
      </p>
      <p><b>（7）把杂打好，但不限于打杂</b></p>
      <p>
        做完一件事，要想想自己做这件事情，有哪些环节没有做好，有没有更快的方法；再往深一点去了解，这个“杂”，在整个业务流程中，是怎么产生的？你有没有可能可以写一个更好的方案去把这个“杂活”给优化掉。
      </p>
      <p>
        <b>建议最好是养成写日报、周报的复盘习惯，</b>哪怕上司没对你作要求，你也可以自己写自己看，这样才能避免你被埋没在无尽的、枯燥的、没有价值的杂活中。
      </p>
