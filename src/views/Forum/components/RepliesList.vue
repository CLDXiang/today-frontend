      <p>一般来说，实习主要分为4类：</p>
      <p><b>第一类：学校安排的实习</b></p>
      <p>这类实习，学校区分的种类以及叫法，有认识实习、生产实习、精工实习等多种多样的叫法。</p>
      <p>
        但这类实习本质都是一样，大多就是带你去逛逛，参观参观；一般的持续时间只是在3~5天，最长也不会超过一个月，然后让你写一篇实习报告。这种实习，不会有工资，不会有什么锻炼机会，也不会有大牛指导的机会，<b>总结一个字，就是“水”。</b>
      </p>
      <p><b>第二类：散招实习</b></p>
      <p>
        所谓散招实习，<b>就是自己准备简历，筛选公司，调查招聘信息；然后自己一个人去投递公司，参加面试；就像自己社招找工作一样</b>。
      </p>
      <p>这种实习，根据企业方的需求的不同，通过的难度也不一致。</p>
      <p>一般要求比较低下的公司，招你进去大多也就是把你当成个打杂的使用；</p>
      <p>
        但是对一些名企大厂以及一些独角兽小厂的某些实习生岗位来说，难度已经和正式员工招聘差不多；<b>你能够得到的培养机会和锻炼机会和难度也是对等的，虽然你也会打杂，但能够直接参与进项目当中，了解业务，能够独立承担一些锻炼性任务，还会有“大牛”导师指导，福利和薪资实际上特别好；散招的实习童鞋如果表现良好，是直接走转正留任的流程的</b>。
      </p>
      <p>
        这种名企大厂以及一些独角兽小厂有锻炼性的实习生岗位，一般还被同学戏称为：<b>“带薪学习”。</b>
      </p>
      <p>一般在大四上之前的同学，想得到高质量的锻炼，只有参加这类实习。</p>
      <p><b>第三类：已有校招offer的实习</b></p>
      <p>
        这类实习是指，在大四上参加完校招，拿到offer之后，如果学生自己愿意，可以提前到签约企业里面进行实习，具体所做的工作实际上和散招实习生差不多；一般工资会比散招实习生的工资高。
      </p>
      <p>这种实习，一般是大四下的学生，且本身已经有过一两段散招实习的经历。</p>
